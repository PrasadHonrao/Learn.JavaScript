// Math library functions
// abs - returns absolute value
console.log(Math.abs(4.2));
console.log(Math.abs(-4.2));


// floor - largest value less than or equal to number
console.log(Math.floor(1.234));
console.log(Math.floor(-1.234));


// ceil - smallest value larger than or equal to number
console.log(Math.ceil(1.234));
console.log(Math.ceil(-1.234));


//pow - 
console.log(Math.pow(2, 3));


//random - returns random number between 0 and 1.
console.log(Math.random());
console.log(Math.random());


//round - returns nearest integer value
console.log(Math.round(-2.2));
console.log(Math.round(2.2));
console.log(Math.round(2.5));
console.log(Math.round(2.6));
console.log(Math.round(2.8));